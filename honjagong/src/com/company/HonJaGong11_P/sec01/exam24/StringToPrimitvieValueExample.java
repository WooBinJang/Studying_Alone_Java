package com.company.HonJaGong11_P.sec01.exam24;

public class StringToPrimitvieValueExample {
    public static void main(String[] args) {
        int value = Integer.parseInt("10"); // String -> int
        double value2 = Double.parseDouble("3.14"); // String -> double
        boolean value3 = Boolean.parseBoolean("true"); // String -> boolean

        System.out.println(value);
        System.out.println(value2);
        System.out.println(value3);
    }
}
